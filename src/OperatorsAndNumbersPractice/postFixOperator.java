package OperatorsAndNumbersPractice;

public class postFixOperator {
    public static void main(String[] args) {
        int x = 30;

        System.out.println("before postfixIncrement: " +x);
        System.out.println("value return by the postfixIncrement expression : " + x++);
        System.out.println("after postfixIncrement: " +x);


    }
}
